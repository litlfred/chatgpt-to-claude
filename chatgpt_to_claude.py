#!/usr/bin/env python3
"""
chatgpt_to_claude.py
====================
Converts a ChatGPT export ZIP into a fully preserved local archive:

  1. One Markdown file per conversation (full history + inline assets)
  2. Binary assets extracted from ZIP or downloaded via OpenAI Files API
  3. Unrecoverable DALL·E images recreated via Claude's vision/image context
  4. Unrecoverable ChatGPT-generated files recreated by Claude from context
  5. Claude-generated summary for every conversation
  6. Master INDEX.md linking all conversations with summaries + asset stats
  7. Optional upload of conversations to Claude API (with deduplication)

Recreation policy
-----------------
  • User-uploaded files  → extracted from ZIP if present; otherwise noted as
                           permanently unrecoverable (OpenAI blocks API download
                           of purpose=user_data files by design).
  • DALL·E images        → extracted from ZIP by size-match first; if missing,
                           recreated via Claude using the original DALL·E prompt
                           and any seed/gen-id metadata preserved in the JSON.
  • Code-interpreter /   → text content preserved inline; binary outputs
    ChatGPT file outputs   recreated by Claude from conversation context.

Recreated artifacts are clearly marked in the Markdown with a banner.

Usage:
    pip3 install anthropic openai
    python3 chatgpt_to_claude.py path/to/export.zip

Environment variables (optional – script will prompt if absent):
    ANTHROPIC_API_KEY
    OPENAI_API_KEY
"""

